main () {
  // Eşitlik ve İlişkisel Operatörler: ==,!=,>,<,>=,<=
  // Kullanıldıkları yerlerde sonucun true veya false çıkmasını sağlar.

  // İki ifade eşitse true değilse false.

  print(5 == 2);

  // Değişkenli kullanım.

  var sonuc = (5 == 2);
  print(sonuc);

  // 4, 2'ye eşit değilse true yazdır eşitse false yazdır.

  print(4 != 2);

  // 5, 2'den büyükse 

  print(5 > 2);

  // 5, 2'den küçükse

  print(5 < 2);
  
}