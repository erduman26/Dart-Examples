main() {
  // int,double type'ları num type'nın alt seviye type'larıdır.

  // int, tüm sayıları ifade eder.

  int sayi = 10;
  print(sayi);

  // double, ondalıklı sayıları ifade eder.

  double ondakik = 3.14;
  print(ondakik);

  // (e5) on üzeri beş anlamına gelir.
  
  var e = 1.42e5;
  print(e);
}