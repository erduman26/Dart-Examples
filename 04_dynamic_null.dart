main () {
  // null boşluk anlamına gelir.
  // null kullanmadan'da kullanabiliriz.

  var deneme;
  print(deneme);

  // null atayarak kullanma şekli.

  var deneme2 = null;
  print(deneme2);

  // dynamik type her türlü değeri alabilir.
  // tam sayı kullanımı.

  dynamic dinamik = 10;
  print(dinamik);

  // ondalıklı sayı kullanımı.

  dynamic dinamik1 = 1.23;
  print(dinamik1);

  // string kullanımı.

  dynamic dinamik3 = "Erdem";
  print(dinamik3);
  
  // bool kullanımı.

  dynamic dinamik4 = true;
  print(dinamik4);
}